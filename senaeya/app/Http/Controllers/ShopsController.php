    {

        $query = Input::get('q');
        $result = Shop::query()
            ->where('name', 'LIKE', "%" . $query . "%")
            ->orWhere('address', 'LIKE', "%" . $query . "%")->get();

        return view('pages.shops')->with('shops', $result);

    }

